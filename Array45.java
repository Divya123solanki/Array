 //Find the first repeating element
 class Array45 {
    public static void main(String args[]){
       int a[]={6,5,1,2,1,3,2,6};
       for(int i=0;i<a.length;i++){
        for(int j=i+1;j<a.length;j++){
            if(a[i]==a[j]){
                System.out.println(a[i]+" ");
            }
            }
            break;
        }
       }
    }

